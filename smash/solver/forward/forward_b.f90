        CASE ('nse') 
          CALL PUSHCONTROL3B(6)
        CASE ('kge') 
          CALL PUSHCONTROL3B(5)
        CASE ('kge2') 
          CALL PUSHREAL4(imd)
          imd = KGE(qo, qs)
          CALL PUSHCONTROL3B(4)
        CASE ('se') 
          CALL PUSHCONTROL3B(3)
        CASE ('rmse') 
          CALL PUSHCONTROL3B(2)
        CASE ('logarithmique') 
