    REAL :: res_b
    REAL :: r, a, b
    REAL :: r_b, a_b, b_b
    INTRINSIC SQRT
    REAL :: temp_b
    CALL KGE_COMPONENTS(x, y, r, a, b)
! KGE criterion
    IF ((r-1)**2 + (b-1)**2 + (a-1)**2 .EQ. 0.0) THEN
      temp_b = 0.0
    ELSE
      temp_b = res_b/(2.0*SQRT((r-1)**2+(b-1)**2+(a-1)**2))
    END IF
    r_b = 2*(r-1)*temp_b
    b_b = 2*(b-1)*temp_b
    a_b = 2*(a-1)*temp_b
    CALL KGE_COMPONENTS_B(x, y, y_b, r, r_b, a, a_b, b, b_b)
  END SUBROUTINE KGE_B

