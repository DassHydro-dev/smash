            CASE (0) 
              CALL PUSHREAL4(states%hp(row, col))
              CALL GR_PRODUCTION(pn, en, parameters%cp(row, col), &
&                          parameters%beta(row, col), states%hp(row, col&
&                          ), pr, perc)
