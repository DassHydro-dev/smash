    IF (.NOT.var_y .EQ. 0.0) var_y_b = var_y_b - temp0*cov*r_b/(2.0*temp&
&       *temp1**2)
    mean_y_b = b_b/mean_x - mean_x*cov_b - 2*mean_y*var_y_b
