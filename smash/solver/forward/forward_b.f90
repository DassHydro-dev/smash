              END IF
            CASE (1) 
              CALL PUSHREAL4(states%hi(row, col))
              CALL GR_INTERCEPTION(prcp, pet, parameters%ci(row, col), &
&                            states%hi(row, col), pn, ei)
              CALL PUSHCONTROL2B(1)
            CASE DEFAULT
              CALL PUSHCONTROL2B(0)
            END SELECT
